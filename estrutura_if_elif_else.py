# Estrutura if-elif-else

idade = 65 # Entrada inteira

if idade <=12:
    print('O invíduo é considerado uma criança.')
elif (12<idade<18):
    print('O invíduo é considerado um adolescente.')
elif (18<=idade<60):
    print('O invíduo é considerado um adulto.')
else:
    print('O invíduo é considerado um idoso.')